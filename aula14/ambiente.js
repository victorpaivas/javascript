var c = 1

//Estrutura de repetição com teste lógico no inicio
while (c <=5) {
    console.log(c)
    c++
}
console.log('Estrutura de repetição com teste lógico no inicio')
//-----------------------------------------------------

//Estrutura de repetição com teste lógico no final
do {
    console.log(c)
    c++
} while (c <=8) 
console.log('Estrutura de repetição com teste lógico no final')
//---------------------------------------------------

//Estrutura de repetição com variável de controle
for (var c=1; c<=10; c++) {
    console.log(c)
}
console.log('Estrutura de repetição com variável de controle')